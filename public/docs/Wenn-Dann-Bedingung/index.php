
<h1>Wenn-Dann-Bedingung</h1>
<p><br />
  Mit Hilfe dieses Aktionstyps lassen sich bestimmte Aktionen bedingt ausführen. In der Informatik würde man von einer &quot;If&quot;-Bedingung sprechen.<br />
Diese Aktion besteht aus zwei Hauptkomponenten, nämlich einer Bedinung (das Wenn) und einer Liste von Aktionen (das Dann). Optional gibt es noch eine zweite Liste von Aktionen, die ausgeführt werden, wenn die Bedingung nicht eintritt (das Sonst).<br />
  <br />
  <img src="001.png" width="342" height="202" /><br />
  <br />
  Die Schwierigkeit, diese Aktion richtig einzusetzen, liegt hauptsächlich in ersterer Komponente - dem Wenn.<br />
